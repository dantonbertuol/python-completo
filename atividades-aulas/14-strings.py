print('Essa é uma string (str) com aspas simples.')
print("Essa é uma string (str) com aspas duplas.")
print("Essa é uma 'string' (str) com aspas duplas e simples em alguma palavra.") # Melhor forma do que com caracter de escape
print('Essa é uma "string" (str) com aspas simples e dupla em alguma palavra.') # Melhor forma do que com caracter de escape
print('Essa é uma \'string\' (str) com aspas simples e caracter de escape.') # \ é um caracter de escape
print(r'Ignorando o caracter \n de escape.') # Se a string comecar por 'r' ela ignora o caracter de escape
