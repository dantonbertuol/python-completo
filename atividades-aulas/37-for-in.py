# Iterando strings
texto = 'Python'
for letra in texto:
    print(letra)

# Iterando com a função range(start, stop, step)
for numero in range(5, 10, 2): #começa em 5, vai até 10 (exclusivo) e com step de 2 em 2
    print(numero)

for numero in range(20, 10, -1): #começa em 20, vai até 10 (exclusivo) e com step de -1 (decrementa)
    print(numero)