'''
Operadores Aritméticos
+  : Adição
-  : Subtração
*  : Multiplicação
/  : Divisão
// : Divisão inteira - resultado arredondado para um inteiro
** : Exponenciação - um número elevado a outro
%  : Resto de divisão (módulo)
() : Controle de precedência
'''

# Operações Básicas
print('Multiplicação (*): ', 10 * 10)
print('Adição (+): ', 10 + 10)
print('Subtração (-): ', 10 - 5)
print('Divisão (/): ', 10 / 2)
print('Multiplicar exibições (repetição): ', 10 * 'B')
print('Concatenação de String (+): ', 'Danton' + ' ' + 'Bertuol')

# Outras operações
print('Divisão Inteira (//): ', 10.9 // 3)
print('Potenciação (**): ', 2 ** 10)
print('Resto de Divisão (%): ', 10 % 3)
print('Controle de Precedência ( ): ', (10+10) * 3, 10 + 10 * 3)
